import { networkInterfaces } from 'node:os'
import { publishAuditEvent } from '@defra/fcp-audit-publisher'
import { snsClient } from '../../sns/client.js'
import { config } from '../../../config/index.js'
import { createLogger } from '../../../logging/logger.js'

const logger = createLogger()

// Audit schema constraint: `ip` must be present and at most 20 characters
// long (covers IPv4 and standard IPv6, but not multi-IP strings).
const MAX_IP_LENGTH = 20

const auditPublishConfig = {
  snsClient,
  sns: { topicArn: config.get('aws.messaging.topics.auditEvents') },
  application: config.get('serviceName'),
  component: config.get('serviceName'),
  environment: config.get('cdpEnvironment'),
  version: '1.0.0',
  generateCorrelationId: true
}

/**
 * Resolves and caches this service's own non-internal IPv4 address, used as
 * the audit `ip` when there is no inbound HTTP request to attribute (e.g.
 * background outbox processing). Falls back to `127.0.0.1` if no external
 * interface is found, so the audit payload always satisfies the mandatory
 * `ip` field.
 * @returns {string}
 */
let cachedServiceIp = null
export const getServiceIp = () => {
  if (cachedServiceIp) {
    return cachedServiceIp
  }
  try {
    const addresses = Object.values(networkInterfaces()).flatMap((addrs) => addrs ?? [])
    const external = addresses.find((addr) => addr.family === 'IPv4' && !addr.internal)
    if (external) {
      cachedServiceIp = external.address
      return cachedServiceIp
    }
  } catch (err) {
    logger.warn(
      { event: { type: 'service_ip_resolution_failed', outcome: 'failure', reason: err.message } },
      'Failed to resolve service IP from network interfaces, falling back to loopback'
    )
  }
  cachedServiceIp = '127.0.0.1'
  return cachedServiceIp
}

/**
 * Normalises a raw IP string to a single, schema-compliant address.
 *  - keeps only the first entry of an `x-forwarded-for` style list
 *  - strips a trailing `:port` from IPv4 (`1.2.3.4:5678` → `1.2.3.4`)
 *  - strips an IPv6 zone id (`fe80::1%eth0` → `fe80::1`)
 *  - returns `''` if the result is empty or longer than 20 chars (caller falls back)
 * @param {string} raw
 * @returns {string}
 */
export const sanitiseIp = (raw) => {
  if (!raw || typeof raw !== 'string') {
    return ''
  }
  let ip = raw.split(',')[0].trim()
  // strip IPv6 zone id
  ip = ip.split('%')[0]
  // strip :port from IPv4 (an IPv6 address contains multiple colons, leave it alone)
  if ((ip.match(/:/g) ?? []).length === 1) {
    ip = ip.split(':')[0]
  }
  if (ip.length === 0 || ip.length > MAX_IP_LENGTH) {
    return ''
  }
  return ip
}

/**
 * Resolve the IP to record on the audit event.
 *  - If an inbound Hapi request is provided, prefer the first
 *    `x-forwarded-for` entry, then `request.info.remoteAddress`.
 *  - If neither yields a usable single IP within the 20-char limit, or no
 *    request is provided (e.g. background outbox processing), fall back to
 *    this service's own IP so the mandatory `ip` field is always populated.
 * @param {object} [request]
 * @returns {string}
 */
export const extractIp = (request) => {
  const forwarded = sanitiseIp(request?.headers?.['x-forwarded-for'])
  if (forwarded) {
    return forwarded
  }
  const remote = sanitiseIp(request?.info?.remoteAddress)
  if (remote) {
    return remote
  }
  return getServiceIp()
}

/**
 * Publishes an audit event via SNS. Failures are logged but not propagated
 * to avoid blocking the primary request flow (fire-and-forget by design).
 * @param {object} event
 * @param {object} [request] - Hapi request object, used to resolve the originating IP
 */
export const sendAuditEvent = async (event, request) => {
  // Spreading `correlationid: undefined` would overwrite the UUID generated by
  // generateCorrelationId in auditPublishConfig, causing Joi validation to fail.
  const sanitisedEvent = { ...event }
  if (sanitisedEvent.correlationid === undefined) { delete sanitisedEvent.correlationid }

  try {
    await publishAuditEvent(sanitisedEvent, { ...auditPublishConfig, ip: extractIp(request) })
  } catch (err) {
    logger.error(
      { event: { type: 'audit_publish_failed', outcome: 'failure', reason: err.message } },
      'Failed to publish audit event'
    )
  }
}
